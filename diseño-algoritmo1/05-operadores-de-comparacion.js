/* operadores relacionales o de comparacion
mayor que: a > b                  a es mayor que b 
menor que: a < b                  a es menor que b
mayor o igual que: a >= b         a es mayor o igual que b
menos o igual que: a <= b         a es menor o igual que b 
igual: a == b                     a es igual que b
distinto: a != b                  a es diferente de b
operador estricto: 

*/

let numero1 = 22;
let numero2 = 15;

console.log("mayor que: " + (numero1 > numero2));
console.log("menor que: " + (numero1 < numero2));
console.log("mayor igual que: " + (numero1 >= numero2));
console.log("menor igual que: " + (numero1 <= numero2));
console.log("igual a: " + (numero1 == numero2));
console.log("diferente a: " + (numero1 != numero2));






